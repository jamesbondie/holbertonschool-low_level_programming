#include <stdio.h>
/**
 * main - Entry point
 *
 * Description: Prints the alphabet in lowercase, then in uppercase,
 *              followed by the character '$'.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
int c;
c = '9';
while (c >= '0')
{
putchar(c);
c--;
}
putchar('\n');
return (0);
}
